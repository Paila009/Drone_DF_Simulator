function compassHUD(azimuth,params)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% COMPASS HUD
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

figure;

set(gcf,'Color',params.bgColor);

hold on;

axis equal;

axis off;

theta = linspace(0,2*pi,500);

x = cos(theta);

y = sin(theta);

plot(x,y,...
'Color',[0.7 0.7 0.7],...
'LineWidth',2);

plot([0 0],[-1 1],...
'Color',[0.4 0.4 0.4]);

plot([-1 1],[0 0],...
'Color',[0.4 0.4 0.4]);

text(0,1.1,'N',...
'Color','w',...
'FontSize',14);

text(1.1,0,'E',...
'Color','w',...
'FontSize',14);

text(0,-1.15,'S',...
'Color','w',...
'FontSize',14);

text(-1.15,0,'W',...
'Color','w',...
'FontSize',14);

xv = sind(azimuth);

yv = cosd(azimuth);

quiver(...
0,...
0,...
xv,...
yv,...
0,...
'Color',...
params.signalColor,...
'LineWidth',...
3,...
'MaxHeadSize',...
0.5);

scatter(0,0,...
120,...
'w',...
'filled');

title('Bearing HUD',...
'Color','w');

text(...
0,...
-1.35,...
sprintf('Bearing : %.1f°',azimuth),...
'Color','w',...
'FontSize',13,...
'HorizontalAlignment','center');

end